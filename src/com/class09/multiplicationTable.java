package com.class09;

public class multiplicationTable {

	public static void main(String[] args) {
		// multiplication table
		for(int a=1; a<10; a++) {
			System.out.println("-------------------");
			for(int b=1; b<10; b++) {
				System.out.println(a+" X " +b+" " +"=" +(a*b));
				
			}
		}
		
	}

}
