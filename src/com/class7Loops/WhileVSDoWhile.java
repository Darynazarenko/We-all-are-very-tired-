package com.class7Loops;

public class WhileVSDoWhile {

	public static void main(String[] args) {
		int num =1;
		while(num<=5){
			System.out.println("Hello");
			num++;
		}
		System.out.println("===========Do While==============");
		int num1=1;
		do {
			System.out.println("Hello");
			num1++;
		}while(num1<=5);
		
		int num3 = 0;
		while(num3<=8) {
			System.out.println(num3);
			num3++;
		
		
	}

}
}